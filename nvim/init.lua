              end,
            },
          },
          { name = 'path' },
        },
        sorting = {
          priority_weight = 2,
          comparators = {
            function(...)
              return cmp_buffer:compare_locality(...)
            end,
            compare.offset,
            compare.exact,
            compare.score,
